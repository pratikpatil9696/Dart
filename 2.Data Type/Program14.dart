

//Bitwise 


void main(){
	

	//Bitwise AND
	{	
	   int x=4;
	   int y=7;
   
   	   print(x&y);
   
   	   int a=14;
   	   int b=72;
   
	   print(a&b);
	}


	//Bitwise OR
	{	
	   int x=10;
	   int y=12;
   
   	   print(x|y);
   
   	   int a=14;
   	   int b=72;
   
	   print(a|b);
	}


	//Bitwise XOR
	{	
	   int x=10;
	   int y=12;
   
   	   print(x^y);
   
   	   int a=14;
   	   int b=72;
   
	   print(a^b);
	}



	//Bitwise LeftShift and RightShift
	{	
	   int a=15;
	   print(a<<2);
	   print(a>>2);

	   int b=43;
	   print(b<<3);

	   int c=72;
	   print(c>>4);
	}
}